package clase_3;

/* Escribir un programa que imprima por pantalla la tabla de valores
de verdad para el or y and por separado.
*/

public class Ejercicio_2 {

	public static void main(String[] args) {
		
		System.out.println("| and |");
		System.out.println("|v|v|v|");
		System.out.println("|v|f|f|");
		System.out.println("|f|v|f|");
		System.out.println("|f|f|f|");
		
		System.out.println("-------");
		
		System.out.println("| or  |");
		System.out.println("|v|v|v|");
		System.out.println("|v|f|v|");
		System.out.println("|f|v|v|");
		System.out.println("|f|f|f|");
		
		
	}

}
















